function init() {
  // click on buttons  Export or Import
  var intervalId = setInterval(getProgressFromServer, 1000);


  // ▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪
  // ▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪
  function getProgressFromServer() {
    $.ajax({
      url     : "/test_get_progress",
      type    : "POST",
      headers : { 'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content') },
      data    : { data_set: "" },
      dataType: 'json',
      // ▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪
      success: function (data) {
        if (data.progress >= 100) {
          clearInterval(intervalId);
          data.progress = 100;
        }
        // Обновление информации о прогрессе на странице
        if (data.progress) $('.oparation_text').text("progress -");
        $('.oparation_percents').text(data.progress);
      },
      // ▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪▪
      error: function (jqXHR, status, errorThrown) {            // ERROR
        console.log('getProgressFromServer > ОШИБКА AJAX запроса: ' + status, jqXHR);
      }
    });

  }

}

